def para(text="", bold=False, align=WD_ALIGN_PARAGRAPH.JUSTIFY,
         size=12, space_before=0, space_after=6, italic=False):
    p = doc.add_paragraph()
    p.paragraph_format.alignment = align
    p.paragraph_format.space_before = Pt(space_before)
    p.paragraph_format.space_after  = Pt(space_after)
    p.paragraph_format.first_line_indent = Cm(1.25) if align == WD_ALIGN_PARAGRAPH.JUSTIFY else Cm(0)
    if text:
        run = p.add_run(text)
        run.bold   = bold
        run.italic = italic
        run.font.name = "Times New Roman"
        run.font.size = Pt(size)
    return p

def heading(text, level=1):
    sizes = {1: 14, 2: 13, 3: 12}
    p = doc.add_paragraph()
    p.paragraph_format.alignment = WD_ALIGN_PARAGRAPH.CENTER if level == 1 else WD_ALIGN_PARAGRAPH.LEFT
    p.paragraph_format.space_before = Pt(12 if level == 1 else 8)
    p.paragraph_format.space_after  = Pt(6)
    p.paragraph_format.first_line_indent = Cm(0)
    run = p.add_run(text)
    run.bold = True
    run.font.name = "Times New Roman"
    run.font.size = Pt(sizes.get(level, 12))
    return p

def bullet(text, indent=0):
    p = doc.add_paragraph()
    p.paragraph_format.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    p.paragraph_format.space_after = Pt(3)
    p.paragraph_format.left_indent = Cm(1.25 + indent * 0.5)
    p.paragraph_format.first_line_indent = Cm(-0.5)
    run = p.add_run(f"• {text}")
    run.font.name = "Times New Roman"
    run.font.size = Pt(12)
    return p

def table_row(tbl, cells_data, bold_first=False, header=False):
    row = tbl.add_row()
    for i, (cell_text, width) in enumerate(cells_data):
        cell = row.cells[i]
        cell.width = Cm(width)
        p = cell.paragraphs[0]
        p.paragraph_format.alignment = WD_ALIGN_PARAGRAPH.LEFT
        p.paragraph_format.space_before = Pt(2)
        p.paragraph_format.space_after  = Pt(2)
        run = p.add_run(cell_text)
        run.font.name = "Times New Roman"
        run.font.size = Pt(11)
        run.bold = header or (bold_first and i == 0)
        if header:
            from docx.oxml import OxmlElement
            tc = cell._tc
            tcPr = tc.get_or_add_tcPr()
            shd = OxmlElement('w:shd')
            shd.set(qn('w:val'), 'clear')
            shd.set(qn('w:color'), 'auto')
            shd.set(qn('w:fill'), 'D9E1F2')
            tcPr.append(shd)

def add_signature_block():
    tbl = doc.add_table(rows=1, cols=2)
    tbl.style = "Table Grid"
    tbl.alignment = WD_TABLE_ALIGNMENT.CENTER
    left  = tbl.cell(0, 0)
    right = tbl.cell(0, 1)
    for cell, side, name in [(left, "Заказчик", "Хохолов А.А."),
                               (right, "Исполнитель", "Ядрихинский С.А.")]:
        cell.width = Cm(8.5)
        lines = [
            (f"{side}:", True),
            ("ГБУ «АНИЦ»" if side == "Заказчик" else "ООО «АСТРА ГРУПП»", True),
            ("", False),
            ("И.о. директора" if side == "Заказчик" else "Генеральный директор", False),
            ("", False),
            (f"___________________ / {name}", False),
            ("М.П.", False),
        ]
        for i, (txt, bd) in enumerate(lines):
            p = cell.paragraphs[0] if i == 0 else cell.add_paragraph()
            p.paragraph_format.space_after = Pt(2)
            run = p.add_run(txt)
            run.bold = bd
            run.font.name = "Times New Roman"
            run.font.size = Pt(11)

